#!/usr/bin/env sh
set -eu

ROOT_DIR=${APP_ROOT:-/app}
cd "$ROOT_DIR"

if [ -f "$ROOT_DIR/router.env.sh" ]; then
  # shellcheck disable=SC1091
  . "$ROOT_DIR/router.env.sh"
fi

ROUTER_DATA_DIR=${ROUTER_DATA_DIR:-"$ROOT_DIR/.router-data"}
ROUTER_PROJECT_NAME=${ROUTER_PROJECT_NAME:-test}
ROUTER_CC_CONNECT_CMD=${ROUTER_CC_CONNECT_CMD:-cc-connect}
ROUTER_CACHE_DIR=${ROUTER_CACHE_DIR:-"$ROUTER_DATA_DIR/.cc-router-cache"}
ROUTE_CONFIG=${ROUTER_ROUTE_CONFIG_FILE:-"$ROUTER_DATA_DIR/config.route.toml"}
