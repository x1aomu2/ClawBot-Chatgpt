LOCAL_ENV=${ROUTER_LOCAL_ENV_FILE:-"$ROUTER_DATA_DIR/router.env.local.sh"}
WEIXIN_SETUP_TIMEOUT=${WEIXIN_SETUP_TIMEOUT:-600}

export ROUTER_DATA_DIR ROUTER_PROJECT_NAME ROUTER_CC_CONNECT_CMD ROUTER_CACHE_DIR
mkdir -p "$ROUTER_DATA_DIR" "$ROUTER_CACHE_DIR"

quote_shell() {
  printf "'"
  printf "%s" "$1" | sed "s/'/'\\\\''/g"
  printf "'"
}

default_env_line() {
  key=$1
  value=$2
  printf 'if [ -z "${%s:-}" ]; then export %s=%s; fi\n' "$key" "$key" "$(quote_shell "$value")"
}

write_local_env() {
  {
    echo "# Generated by docker-entrypoint.sh. Keep this file out of Git."
    for key in \
      WEIXIN_TOKEN \
      WEIXIN_ALLOW_FROM \
      WEIXIN_ADMIN_FROM \
      WEIXIN_ACCOUNT_ID \
      WEIXIN_CDN_BASE_URL \
      OPENAI_API_KEY \
      ROUTER_IMAGE_API_KEY \
      ROUTER_IMAGE_ENDPOINT \
      ROUTER_IMAGE_PROVIDER \
      ROUTER_IMAGE_MODEL \
      ROUTER_IMAGE_API \
      ROUTER_IMAGE_SIZE \
      ROUTER_IMAGE_QUALITY \
      ROUTER_IMAGE_CONTEXT_TTL_MINS \
      ROUTER_IMAGE_CONTEXT_MAX \
      ROUTER_CODE_API_KEY \
      ROUTER_CODE_ENDPOINT \
      ROUTER_CODE_PROVIDER \
      ROUTER_CODE_MODEL \
      ROUTER_CODE_TEMPERATURE \
      ROUTER_CODE_MAX_OUTPUT_TOKENS \
      ROUTER_CODE_REASONING_EFFORT
    do
      eval "value=\${$key:-}"
      default_env_line "$key" "$value"
    done
  } > "$LOCAL_ENV"
}

toml_string() {
  node -e 'process.stdout.write(JSON.stringify(String(process.argv[1]).replace(/\\/g, "/")))' "$1"
}

