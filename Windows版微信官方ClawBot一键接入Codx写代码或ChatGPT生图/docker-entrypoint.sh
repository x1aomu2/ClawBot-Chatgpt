extract_toml_value() {
  node - "$1" "$2" <<'NODE'
const fs = require("fs");
const file = process.argv[2];
const key = process.argv[3];
let text = "";
try {
  text = fs.readFileSync(file, "utf8");
} catch {
  process.exit(0);
}
const escapedKey = key.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
const match = text.match(new RegExp(`(?:^|\\n)\\s*${escapedKey}\\s*=\\s*"([^"]*)"`));
if (match && match[1] && !match[1].startsWith("${")) {
  process.stdout.write(match[1]);
}
NODE
}

extract_weixin_token_from_data() {
  node - "$ROUTER_DATA_DIR" <<'NODE'
const fs = require("fs");
const path = require("path");
const root = path.join(process.argv[2], "weixin");
const files = [];

function walk(dir) {
  let entries = [];
  try {
    entries = fs.readdirSync(dir, { withFileTypes: true });
  } catch {
    return;
  }
  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      walk(fullPath);
    } else if (entry.name === "get_updates.buf") {
      try {
        files.push({ fullPath, mtimeMs: fs.statSync(fullPath).mtimeMs });
      } catch {
      }
    }
  }
}

walk(root);
files.sort((a, b) => b.mtimeMs - a.mtimeMs);

for (const file of files) {
  try {
    const raw = fs.readFileSync(file.fullPath, "utf8").trim();
    if (!raw) continue;
    const decoded = Buffer.from(raw, "base64").toString("utf8");
    const match = decoded.match(/([A-Za-z0-9._-]+@im\.[A-Za-z0-9.-]+:[A-Za-z0-9]+)/);
    if (match) {
      process.stdout.write(match[1]);
      process.exit(0);
    }
  } catch {
  }
}
NODE
}

extract_weixin_admin_from_data() {
  node - "$ROUTER_DATA_DIR" <<'NODE'
const fs = require("fs");
const path = require("path");
const root = process.argv[2];
const files = [];
const userRe = /([A-Za-z0-9._-]+@im\.wechat)/;

function walk(dir) {
  let entries = [];
  try {
    entries = fs.readdirSync(dir, { withFileTypes: true });
  } catch {
    return;
  }
  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      walk(fullPath);
    } else if (entry.name === "context_tokens.json" || fullPath.includes(`${path.sep}sessions${path.sep}`)) {
      try {
        files.push({ fullPath, mtimeMs: fs.statSync(fullPath).mtimeMs });
      } catch {
      }
    }
  }
}

walk(root);
files.sort((a, b) => b.mtimeMs - a.mtimeMs);

for (const file of files) {
  try {
    const text = fs.readFileSync(file.fullPath, "utf8");
    const match = text.match(userRe);
    if (match) {
      process.stdout.write(match[1]);
      process.exit(0);
    }
  } catch {
  }
}
NODE
}

ensure_account_values() {
  if [ -z "${WEIXIN_ALLOW_FROM:-}" ]; then
    WEIXIN_ALLOW_FROM='*'
    export WEIXIN_ALLOW_FROM
  fi

  if [ -z "${WEIXIN_CDN_BASE_URL:-}" ]; then
    WEIXIN_CDN_BASE_URL='https://novac2c.cdn.weixin.qq.com/c2c'
    export WEIXIN_CDN_BASE_URL
  fi

  if [ -z "${WEIXIN_ACCOUNT_ID:-}" ] && [ -n "${WEIXIN_TOKEN:-}" ]; then
    WEIXIN_ACCOUNT_ID=${WEIXIN_TOKEN%%:*}
    export WEIXIN_ACCOUNT_ID
  fi
}

validate_api_env() {
  missing=""

  if [ -z "${ROUTER_IMAGE_API_KEY:-}" ] && [ -z "${OPENAI_API_KEY:-}" ]; then
    missing="$missing ROUTER_IMAGE_API_KEY-or-OPENAI_API_KEY"
  fi
  if [ -z "${ROUTER_CODE_API_KEY:-}" ] && [ -z "${OPENAI_API_KEY:-}" ]; then
    missing="$missing ROUTER_CODE_API_KEY-or-OPENAI_API_KEY"
  fi
  case ${ROUTER_IMAGE_ENDPOINT:-} in
    ""|*your-api.example*) missing="$missing ROUTER_IMAGE_ENDPOINT" ;;
  esac
  case ${ROUTER_CODE_ENDPOINT:-} in
    ""|*your-api.example*) missing="$missing ROUTER_CODE_ENDPOINT" ;;
  esac

  if [ -n "$missing" ]; then
    echo "Missing required environment values:$missing" >&2
    echo "Copy .env.example to .env, fill it, then run: sh deploy.sh up" >&2
    return 1
  fi
}

run_weixin_setup() {
  command_name=setup
  if [ "${WEIXIN_FORCE_REBIND:-0}" = "1" ]; then
    command_name=new
    case "$ROUTER_DATA_DIR" in
      "$ROOT_DIR"/*) rm -rf "$ROUTER_DATA_DIR/weixin" ;;
      *) echo "Refusing to clear WEIXIN state outside APP_ROOT: $ROUTER_DATA_DIR" >&2 ;;
    esac
  fi

  rm -f "$QR_IMAGE" "$SETUP_CONFIG"
  write_setup_config

  echo "WEIXIN_TOKEN is empty. Starting Weixin QR setup..."
  echo "QR image will be saved to: $QR_IMAGE"
  echo "Open that file from the Mac host and scan it with Weixin."

  set +e
  "$ROUTER_CC_CONNECT_CMD" weixin "$command_name" \
    --config "$SETUP_CONFIG" \
    --project "$ROUTER_PROJECT_NAME" \
    --qr-image "$QR_IMAGE" \
    --timeout "$WEIXIN_SETUP_TIMEOUT" \
    --set-allow-from-empty
  setup_status=$?
  set -e

  token=$(extract_toml_value "$SETUP_CONFIG" token || true)
  if [ -z "$token" ]; then
    token=$(extract_weixin_token_from_data || true)
  fi

  if [ -z "$token" ]; then
    echo "Weixin setup finished without a token. Send one Weixin message to the bot, then restart." >&2
    if [ "$setup_status" -eq 0 ]; then
      setup_status=1
    fi
    return "$setup_status"
  fi

  WEIXIN_TOKEN=$token
  export WEIXIN_TOKEN

  admin_from=$(extract_toml_value "$SETUP_CONFIG" admin_from || true)
  if [ -z "$admin_from" ]; then
    admin_from=$(extract_weixin_admin_from_data || true)
  fi
  if [ -n "$admin_from" ]; then
    WEIXIN_ADMIN_FROM=$admin_from
    export WEIXIN_ADMIN_FROM
  fi

  ensure_account_values
  write_local_env

  if [ "$setup_status" -ne 0 ]; then
    echo "Weixin setup exited with $setup_status, but token was saved. Continuing..."
  fi
}

start_router() {
  if ! validate_api_env; then
    exec tail -f /dev/null
  fi
  ensure_account_values

  if [ -z "${WEIXIN_TOKEN:-}" ] || [ "${WEIXIN_FORCE_REBIND:-0}" = "1" ]; then
    if ! run_weixin_setup; then
      exec tail -f /dev/null
    fi
  fi

  ensure_account_values
  write_local_env
  write_route_config

  echo "Starting cc-connect route service..."
  echo "Config: $ROUTE_CONFIG"
  echo "Data:   $ROUTER_DATA_DIR"
  exec "$ROUTER_CC_CONNECT_CMD" --config "$ROUTE_CONFIG" --force
}

case "${1:-start}" in
  start)
    start_router
    ;;
  setup)
    validate_api_env
    ensure_account_values
    run_weixin_setup
    ;;
  sh|shell)
    exec sh
    ;;
  *)
    exec "$@"
    ;;
esac
